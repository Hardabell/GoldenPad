package com.goldenpad.goldenpad_;

public class custom_list {

        int image;
        String name, team;

        public custom_list(int image, String name, String team) {
            this.image = image;
            this.name = name;
            this.team = team;
        }

        public int getImage() {
            return image;
        }

        public String getName() {
            return name;
        }

        public String getTeam() {
            return team;
        }
    }


